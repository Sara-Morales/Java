
package Clase_8.Empelado;

public class Tiempo extends Empleado{
    
    public int horas=0;
    public int minutos=0;
    public int segundos =0;
    
    public void establecerHora(int hora, int minuto, int segundo){
        this.horas =hora;
        this.minutos=minuto;
        this.segundos =segundo;
        
    }
}
